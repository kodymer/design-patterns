using System;

namespace SingletonPattern
{
    class Program
    {
        /// <summary>
        /// Singleton: trata sobre una clase diseñada para que solo 
        /// tenga una unica instancia
        /// 
        /// Caracteristicas:
        /// - En cualquier momento, 0 o 1 instancia de una clase singleton
        /// existe en la app
        /// - Las clases singleton son creadas sin parametros
        /// - Por defecto, asume una instanciación perezosa
        /// </summary>
        static void Main(string[] args)
        {
            Console.WriteLine("Hello World!");
        }
    }
}
